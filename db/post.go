func InsertBlogPost(p *data.Post) (int, error) {
	query := `INSERT INTO post(creator, title, tags, content, created_at)
                VALUES($1, $2, $3, $4, $5)
                RETURNING id`

	var pk int

	err := db.QueryRow(query, p.Creator, p.Title, pq.Array(p.Tags), p.Content, time.Now()).Scan(&pk)
	if err != nil {
		log.Println(err)
		return 0, err
	}

	p.ID = pk
	return pk, nil
}

